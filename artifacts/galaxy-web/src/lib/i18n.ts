let currentLang: Language = (localStorage.getItem("galaxy_lang") as Language) || "en";
const listeners: Array<(lang: Language) => void> = [];

export function t(key: string): string {
  return translations[currentLang]?.[key] || translations.en[key] || key;
}

export function getCurrentLanguage(): Language {
  return currentLang;
}

export function setLanguage(lang: Language): void {
  currentLang = lang;
  localStorage.setItem("galaxy_lang", lang);
  listeners.forEach(cb => cb(lang));
}

export function onLanguageChange(cb: (lang: Language) => void): () => void {
  listeners.push(cb);
  return () => {
    const idx = listeners.indexOf(cb);
    if (idx >= 0) listeners.splice(idx, 1);
  };
}

export function isRTL(): boolean {
  return currentLang === "ar" || currentLang === "ur";
}

